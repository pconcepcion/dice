// Helper Functions

// equalTokenSlices compares two token slices and returns true if both are have the same content
func assertEqualTokenSlices(a, b []Token) bool {
	if len(a) != len(b) {
		return false
	}
	// TODO: extracth this to a compare token arrays function
	for i, tok := range a {
		if tok != b[i] {
			return false
		}
	}
	return true
}

